package com;

public class ChangePass {

	String user,cpwd,npwd;

	public ChangePass(String user, String cpwd, String npwd) {
		super();
		this.user = user;
		this.cpwd = cpwd;
		this.npwd = npwd;
	}

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public String getCpwd() {
		return cpwd;
	}

	public void setCpwd(String cpwd) {
		this.cpwd = cpwd;
	}

	public String getNpwd() {
		return npwd;
	}

	public void setNpwd(String npwd) {
		this.npwd = npwd;
	}
	
	
	

}
